#include <iostream>

using namespace std;

int main()
{
    cout << "Hello world!" << endl;

    int i = 0;

    //for loop

    for (int i = 0; i < 11; i++){
        cout<<i<<"\n";

    }

    // while loop

    while (i < 11) {
        cout<<i<<"\n";
        i++;
    }

    return 0;
}
